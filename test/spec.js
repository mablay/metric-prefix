const test = require('tape')
const metric = require('../')

test('floor', t => {
  t.equal(metric(7), '7.00', 'no conversion')
  t.equal(metric(12, 2), '12', 'no conversion')
  t.equal(metric(999), '999', 'no conversion')
  t.equal(metric(1001, 4), '1.001k', 'kilo converstion')
  t.equal(metric(1009), '1.00k', 'kilo converstion')
  t.equal(metric(12345), '12.3k', 'kilo converstion')
  t.equal(metric(999999), '999k', 'mega converstion')
  t.equal(metric(1e6), '1.00M', 'mega converstion')
  t.equal(metric(1e6 + 9, 7), '1.000009M', 'mega converstion')
  t.equal(metric(1e6 + 9, 6), '1.00000M', 'mega converstion')
  t.equal(metric(1.1e9), '1.10G', 'giga converstion')
  t.equal(metric(10.9e9), '10.9G', 'giga converstion')
  t.equal(metric(100e15), '100P', 'peta converstion')
  t.end()
})
